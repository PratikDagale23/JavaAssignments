public class CheckStringContainsE {
    public static void main(String[] args) {
        String word = "umbrella";

        if (word.contains("e")) {
            System.out.println("The string contains 'e' in the word 'umbrella'.");
        } else {
            System.out.println("The string does not contain 'e' in the word 'umbrella'.");
        }
    }
}
